export function translateBlockString(value, locale) {
  if (locale !== 'en' || typeof value !== 'string') {
    return value;
  }
  return RU_TO_EN[value] ?? value;
}

export function localizeBlockConfigs(configs, locale) {
  if (locale !== 'en') {
    return configs;
  }

  const seen = new WeakSet();

  const walk = (value) => {
    if (typeof value === 'string') {
      return translateBlockString(value, locale);
    }

    if (Array.isArray(value)) {
      return value.map(walk);
    }

    if (value && typeof value === 'object') {
      if (seen.has(value)) {
        return value;
      }
      seen.add(value);

      const next = {};
      for (const [key, nested] of Object.entries(value)) {
        next[key] = walk(nested);
      }
      return next;
    }

    return value;
  };

  return walk(configs);
}
